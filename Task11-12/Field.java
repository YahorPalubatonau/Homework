package HomeWork11_12;

public class Field {

	private float id;
	private int value;

	public Field() {

	}

	public Field(float id, int value) {
		this.id = id;
		this.value = value;
	}

	public float getId() {
		return id;
	}

	public void setId(float id) {
		this.id = id;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

}
